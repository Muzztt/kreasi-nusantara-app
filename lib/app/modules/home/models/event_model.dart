class Event {
  final String title;
  final String category;
  final String location;
  final String date;
  final String price;
  final String button;
  final String imageUrl;

  Event({
    required this.title,
    required this.category,
    required this.location,
    required this.date,
    required this.button,
    required this.price,
    required this.imageUrl,
  });
}
